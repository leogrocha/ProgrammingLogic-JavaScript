function porcentagem(valor_por_cento, numero) {
    return (valor_por_cento * numero) / 100;
}

console.log(porcentagem(50,20));
console.log(porcentagem(25,60));
console.log(porcentagem(20,200));
console.log(porcentagem(10,130));
console.log(porcentagem(1,2400));
console.log(porcentagem(8,43));
console.log(porcentagem(7.5,10.8));